package basic;

public class OpTest06 {
	public static void main(String[] args) {
		int a = 5;
		a += 5;
		System.out.println(a);
		
		int b = 5;
		b -= 5;
		System.out.println(b);
		
		int c = 5;
		c *= 5;
		System.out.println(c);
		
		int d = 5;
		d /= 5;
		System.out.println(d);
		
		int e = 5;
		e %= 5;
		System.out.println(e);
	}
}
